//Comparador estricto

const puntaje = 1001;
const puntaje2 = "1000";
//!= diferente, == igual a

//Comparador no estricto
if (puntaje != 1000) {
  console.log("Sí es diferente");
}

//Comparador stricto
if (puntaje2 === 1000) {
  console.log("Sí es igual");
} else {
  console.log("No es igual");
}
